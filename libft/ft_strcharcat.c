#include "libft.h"

char	*ft_strcharcat(char *s1, const char s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	s1[i] = s2;
	i++;
	s1[i] = '\0';
	return (s1);
}
